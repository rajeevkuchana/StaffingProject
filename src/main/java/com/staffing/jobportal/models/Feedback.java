package com.staffing.jobportal.models;


public class Feedback {
	

	private String shortFeedback;
	
	private String longFeedback;

	public String getShortFeedback() {
		return shortFeedback;
	}

	public void setShortFeedback(String shortFeedback) {
		this.shortFeedback = shortFeedback;
	}

	public String getLongFeedback() {
		return longFeedback;
	}

	public void setLongFeedback(String longFeedback) {
		this.longFeedback = longFeedback;
	}

	
	
	

}

