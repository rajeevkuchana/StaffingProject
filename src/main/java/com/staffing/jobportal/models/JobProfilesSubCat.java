package com.staffing.jobportal.models;

public class JobProfilesSubCat {

	private String subCategoryCode;
	
	private String subCategoryDisplay;

	private String subCategoryDesc;
	
	private String subCategoryIcon;

	public String getSubCategoryDisplay() {
		return subCategoryDisplay;
	}

	public void setSubCategoryDisplay(String subCategoryDisplay) {
		this.subCategoryDisplay = subCategoryDisplay;
	}

	public String getSubCategoryCode() {
		return subCategoryCode;
	}

	public void setSubCategoryCode(String subCategoryCode) {
		this.subCategoryCode = subCategoryCode;
	}

	public String getSubCategoryDesc() {
		return subCategoryDesc;
	}

	public void setSubCategoryDesc(String subCategoryDesc) {
		this.subCategoryDesc = subCategoryDesc;
	}

	public String getSubCategoryIcon() {
		return subCategoryIcon;
	}

	public void setSubCategoryIcon(String subCategoryIcon) {
		this.subCategoryIcon = subCategoryIcon;
	}

	
}
